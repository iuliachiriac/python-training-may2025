"""This module exemplifies some basic Python concepts - this is a docstring"""

print("hello world")

x = 5  # initialize int variable
print(x)

# explicit line joining
message = "A physical line is what you see as a single line in the program" \
          " whereas a logical line is what Python sees as a single line in" \
          " the program or more appropriately, what Python sees as a single" \
          " statement in the program."

# implicit line joining
print(
    message,
    x,
    "hello"
)


